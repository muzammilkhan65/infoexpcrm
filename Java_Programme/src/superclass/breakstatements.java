package superclass;

public class breakstatements {

	public static void main(String[] args) {
			/*java break is used to break the loop or switch statements, its break the continue flow of the programme 
		    at specified conditions,  in case of inner loop its  break only inner loop. */
		
		for(int i=0; i<=15; i++){
			
			if (i==4){
				break;
			}
		System.out.println(i);	
		}
	}

}

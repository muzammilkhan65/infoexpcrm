package superclass;

public class continuestatements {

	public static void main(String[] args) {
		/*java continues statement is used to continue the loop, its continue the current flow of the programme
		   and skips the specified conditions, in case of inner loop its continue only inner loop, */
	for (int i=1; i<=10; i++){
		
		if(i==4){
			
			continue;
		}
		System.out.println(i);
	}
	
	}

	}
